    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        crow = tbUser.getSelectedRow();
        int id_user = 0;
        if (crow < 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để xóa!");
        } else {
            id_user = Integer.parseInt(tbUser.getValueAt(crow, 0).toString());
            User objUser = new User(id_user, "", "", "", "", "", "", "", null, "","");
            int p = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa", "Xóa?", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                controller.delItem(crow, objUser);
                JOptionPane.showMessageDialog(this, "Xóa thàng công!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

