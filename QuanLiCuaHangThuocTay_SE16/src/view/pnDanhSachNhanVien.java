    private void btCatFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCatFindActionPerformed
        TableRowSorter sorter = new TableRowSorter(tbUser.getModel());
        tbUser.setRowSorter(sorter);

        String id = tfIdFind.getText();
        String chucvu = tfChucVuFind.getText();
        String name = tfNameFind.getText();

        RowFilter<AbstractTableModel, Object> fillterID = RowFilter.regexFilter(id, 0);
        RowFilter<AbstractTableModel, Object> fillterChucVu = RowFilter.regexFilter("(?i)" + chucvu, 3);
        RowFilter<AbstractTableModel, Object> fillterName = RowFilter.regexFilter("(?i)" + name, 1);

        ArrayList<RowFilter<AbstractTableModel, Object>> alFillter = new ArrayList<>();
        alFillter.add(fillterID);
        alFillter.add(fillterName);
        alFillter.add(fillterChucVu);

        RowFilter<AbstractTableModel, Object> fillterAnd = RowFilter.andFilter(alFillter);
        sorter.setRowFilter(fillterAnd);
    }//GEN-LAST:event_btCatFindActionPerformed

