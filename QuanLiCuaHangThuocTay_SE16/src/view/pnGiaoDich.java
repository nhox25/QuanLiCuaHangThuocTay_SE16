    private void btThemGiamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemGiamActionPerformed
        crow = tbSanPham.getSelectedRow();
        btInHoaDon.setEnabled(false);
        btTinhTien.setEnabled(true);
        int crow1 = -1;
        crow1 = tbHoaDon.getSelectedRow();
        HoaDon hoadon = null;
        int soluong = (int) spSoluong.getValue();
        if (crow < 0 && crow1 < 0) {
            JOptionPane.showMessageDialog(null, "Chọn món cần thêm");
            return;
        }
        if (soluong <= 0) {
            JOptionPane.showMessageDialog(null, "Nhập vào số lớn hơn 0");
            return;
        } else {
            int thanhtien = 0;
            String tenKH = tfTenKhachHang.getText();
            String diachi = tfDiaChi.getText();
            thanhtien = Integer.parseInt(tfGia.getText()) * soluong;
            int gia = Integer.parseInt(tfGia.getText());
            String tenSP = tfTenSanPham.getText();
            String ghichu = taTTT.getText();
            Date date = new Date();
            Date date1 = new java.sql.Date(date.getTime());
            hoadon = new HoaDon(0, tenKH, diachi, tenSP, soluong, gia, thanhtien, ghichu, (java.sql.Date) date1, nhanvien);
        }
        if (controllerHD.add(hoadon) > 0) {
            loadTableHoaDon(hoadon);
            thanhtoan();
        }
    }//GEN-LAST:event_btThemGiamActionPerformed

