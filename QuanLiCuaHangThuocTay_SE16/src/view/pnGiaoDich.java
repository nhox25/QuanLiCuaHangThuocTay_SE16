    private void btMenuFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMenuFindActionPerformed
        String name = tfTenFind.getText();

        //kiếm theo id_news, tên
        TableRowSorter sorter = new TableRowSorter(tbSanPham.getModel());
        tbSanPham.setRowSorter(sorter);
        RowFilter<AbstractTableModel, Object> filterUserName = RowFilter.regexFilter("(?i)" + name, 1);
        ArrayList<RowFilter<AbstractTableModel, Object>> alFilter = new ArrayList<>();
        alFilter.add(filterUserName);

        //kiếm theo combobox
        String id_find_chooser = (String.valueOf(cbLoaiFind.getSelectedItem()));
        if (cbLoaiFind.getSelectedIndex() != 0) {
            RowFilter<AbstractTableModel, Object> filterLoai = RowFilter.regexFilter(id_find_chooser, 2);
            alFilter.add(filterLoai);
        }
        RowFilter<AbstractTableModel, Object> filterAnd = RowFilter.andFilter(alFilter);
        sorter.setRowFilter(filterAnd);
    }//GEN-LAST:event_btMenuFindActionPerformed

