    private void btTinhTienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTinhTienActionPerformed
        int tongtien = 0;
        double phaitra = 0;
        ArrayList<HoaDon> thanhtoan = modelHoaDon.getList();
        if (tfGiamGia.getText().equals("")) {
            tfGiamGia.setText("0");
        }
        if (tfGiamPT.getText().equals("")) {
            tfGiamPT.setText("0");
        }
        double giamgiaTM = Double.parseDouble(tfGiamGia.getText());
        double giamPhanTram = Double.parseDouble(tfGiamPT.getText());
        for (int i = 0; i < thanhtoan.size(); i++) {
            tongtien += thanhtoan.get(i).getSoluong() * (thanhtoan.get(i).getGia());
        }
        double GiamPT = (float) giamPhanTram * tongtien / 100;
        phaitra = (double) tongtien - giamgiaTM - GiamPT;
        if (phaitra < 0) {
            tfPhaiTra.setText(String.valueOf(phaitra));
            JOptionPane.showMessageDialog(null, "Giảm giá hơi sai sai :((?");
        } else {
            tfPhaiTra.setText(String.valueOf(phaitra));
        }
//        tongtien = tongtien;
        tfTongTien.setText(String.valueOf(tongtien));
        btInHoaDon.setEnabled(true);
        ttien = Double.parseDouble(tfTongTien.getText());
        ptra = Double.parseDouble(tfPhaiTra.getText());
        giamgia = Double.parseDouble(tfGiamGia.getText());
        giamgiaPT = Double.parseDouble(tfGiamPT.getText());
        tenKH = tfTenKhachHang.getText();
        diachi = tfDiaChi.getText();
    }//GEN-LAST:event_btTinhTienActionPerformed
