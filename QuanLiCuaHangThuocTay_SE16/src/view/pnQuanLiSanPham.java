    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        int id = 0;
        String name = tfName.getText();
        String donvi = tfDonVi.getText();
        String solo = tfSolo.getText();
        Date date = (Date) spHanDung.getValue();
        Timestamp hangdung = new Timestamp(date.getTime());
        String soluong = tfSoluong.getText();
        String dongia = tfGiaBan.getText();
        LoaiThucPham loai = (LoaiThucPham) cbLoai.getSelectedItem();
        SanPham obj = new SanPham(id, name, donvi, solo, hangdung, soluong, dongia,loai.getId_loai(),loai);
        if (isValid("add", obj)) {
            if (controller.addItem(obj) > 0) {
                resetForm();
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Có lỗi khi thêm");
            }
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int id = 0;
        row = tbSanPham.getSelectedRow();
        if (!tfID.getText().isEmpty()) {
            id = Integer.parseInt(tfID.getText());
        }
        String name = tfName.getText();
        String donvi = tfDonVi.getText();
        String solo = tfSolo.getText();
        Date date = (Date) spHanDung.getValue();
        Timestamp hangdung = new Timestamp(date.getTime());
        String soluong = tfSoluong.getText();
        String dongia = tfGiaBan.getText();
        LoaiThucPham loai = (LoaiThucPham) cbLoai.getSelectedItem();
        SanPham obj = new SanPham(id, name, donvi, solo, hangdung, soluong, dongia,loai.getId_loai(),loai);
        if (isValid("edit", obj)) {
            int p = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa", "Sửa?", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                controller.edit(row, obj);
                JOptionPane.showMessageDialog(this, "Sửa thàng công!");
                resetForm();
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

