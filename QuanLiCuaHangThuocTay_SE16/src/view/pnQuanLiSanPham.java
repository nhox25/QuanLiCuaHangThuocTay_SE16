    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        row = tbSanPham.getSelectedRow();
        int id = 0;
        if (!tfID.getText().isEmpty()) {
            id = Integer.parseInt(tfID.getText());
        }
        SanPham obj = new SanPham(id, "", "", "", null, "", "",0,null);
        if (isValid("del", obj)) {
            int p = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa", "Xóa?", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                controller.delItem(row, obj);
                JOptionPane.showMessageDialog(this, "Xóa thàng công!");
                resetForm();
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

