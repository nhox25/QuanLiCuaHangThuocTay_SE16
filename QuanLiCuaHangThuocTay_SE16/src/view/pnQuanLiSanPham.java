    public void setForm() {
        row = tbSanPham.getSelectedRow();
        tfID.setText(tbSanPham.getValueAt(row, 0).toString());
        tfName.setText(tbSanPham.getValueAt(row, 1).toString());
        loadLoai((LoaiThucPham) tbSanPham.getValueAt(row, 2), false);
        tfDonVi.setText(tbSanPham.getValueAt(row, 3).toString());
        tfSolo.setText(tbSanPham.getValueAt(row, 4).toString());
        spHanDung.setValue(tbSanPham.getValueAt(row, 5));
        tfSoluong.setText(tbSanPham.getValueAt(row, 6).toString());
        tfGiaBan.setText(tbSanPham.getValueAt(row, 7).toString());
    }
    public void loadLoai(LoaiThucPham loaiSelected, boolean isSearch) {
        cbLoai.setModel(new ComboBoxModelLoaiThucPham (loaiSelected, false));
    }
    public void resetForm() {
        row = -1;
        tfID.setText("");
        tfName.setText("");
        tfDonVi.setText("");
        tfSolo.setText("");
        tfGiaBan.setText("");
        tfSoluong.setText("");
    }

    private boolean isValid(String func, SanPham obj) {
        boolean result = true;
        switch (func) {
            case "add":
                if (obj.getNameSP().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập tên thực phẩm");
                    return false;
                }
                if (obj.getDonvi().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đơn vị");
                    return false;
                }
                if (obj.getSolo().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số lô");
                    return false;
                }
                if (obj.getSoluong().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng");
                    return false;
                } else {
                    try {
                        Integer.parseInt(tfSoluong.getText());
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Nhập số lượng là số");
                        return false;
                    }
                }
                if (obj.getDongia().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập giá bán");
                    return false;
                } else {
                    try {
                        Integer.parseInt(tfGiaBan.getText());
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Nhập giá bán là số");
                        return false;
                    }
                }
                break;
            case "del":
                if (obj.getIdSanPham() <= 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để xóa!");
                    result = false;
                }
                break;
            case "edit":
                if (obj.getIdSanPham() <= 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để sửa!");
                    return false;
                }
                if (obj.getNameSP().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập tên sản phẩm");
                    return false;
                }
                if (obj.getDonvi().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đơn vị");
                    return false;
                }
                if (obj.getSolo().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số lô");
                    return false;
                }
                if (obj.getSoluong().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng");
                    return false;
                } else {
                    try {
                        Integer.parseInt(tfSoluong.getText());
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Nhập số lượng là số");
                        return false;
                    }
                }
                if (obj.getDongia().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập giá bán");
                    return false;
                } else {
                    try {
                        Integer.parseInt(tfGiaBan.getText());
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Nhập giá bán là số");
                        return false;
                    }
                }
                break;
        }
        return result;
    }

