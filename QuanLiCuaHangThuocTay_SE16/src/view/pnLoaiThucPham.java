    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        int id = 0;
        String name = tfLoaiThucPham.getText();
        LoaiThucPham obj = new LoaiThucPham(id, name);
        if (isValid("add", obj)) {
            if (controller.addItem(obj) > 0) {
                resetForm();
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Có lỗi khi thêm");
            }
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int id = 0;
        row = tbLoaiThucPham.getSelectedRow();
        if (!tfIdThucPham.getText().isEmpty()) {
            id = Integer.parseInt(tfIdThucPham.getText());
        }
        String name = tfLoaiThucPham.getText();
        LoaiThucPham obj = new LoaiThucPham(id, name);
        if (isValid("edit", obj)) {
            int p = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa", "Sửa?", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                controller.edit(row, obj);
                JOptionPane.showMessageDialog(this, "Sủa thàng công!");
                resetForm();
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

