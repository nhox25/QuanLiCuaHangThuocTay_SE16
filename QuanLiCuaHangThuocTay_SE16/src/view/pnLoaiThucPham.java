    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        row = tbLoaiThucPham.getSelectedRow();
        int id = 0;
        if (!tfIdThucPham.getText().isEmpty()) {
            id = Integer.parseInt(tfIdThucPham.getText());
        }
        LoaiThucPham obj = new LoaiThucPham(id, "");
        if (isValid("del", obj)) {
            int p = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa", "Xóa?", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                controller.delItem(row, obj);
                JOptionPane.showMessageDialog(this, "Xóa thàng công!");
                resetForm();
            } else {
                JOptionPane.showMessageDialog(null, "Có lỗi trong quá trình xóa");
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        TableRowSorter sorter = new TableRowSorter(tbLoaiThucPham.getModel());
        tbLoaiThucPham.setRowSorter(sorter);

        String id_cat = tfIdThucPhamFind.getText();
        String name = tfLoaiThucPhamFind.getText();

        RowFilter<AbstractTableModel, Object> fillterID = RowFilter.regexFilter(id_cat, 0);
        RowFilter<AbstractTableModel, Object> fillterName = RowFilter.regexFilter("(?i)" + name, 1);

        ArrayList<RowFilter<AbstractTableModel, Object>> alFillter = new ArrayList<>();
        alFillter.add(fillterID);
        alFillter.add(fillterName);

        RowFilter<AbstractTableModel, Object> fillterAnd = RowFilter.andFilter(alFillter);
        sorter.setRowFilter(fillterAnd);
    }//GEN-LAST:event_btSearchActionPerformed

