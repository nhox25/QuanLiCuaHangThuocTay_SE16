    private boolean isValid(String func, LoaiThucPham obj) {
        boolean result = true;
        switch (func) {
            case "add":
                if (obj.getLoai_thucpham().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập loại thực phẩm");
                    result = false;
                }
                break;
            case "del":
                if (obj.getId_loai()<= 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để xóa!");
                    result = false;
                }
                break;
            case "edit":
                if (obj.getLoai_thucpham().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập loại thực phẩm");
                    result = false;
                }
                if (obj.getId_loai()<= 0) {
                    JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để sửa!");
                    result = false;
                }
                break;
        }
        return result;
    }

    public void setForm() {
        row = tbLoaiThucPham.getSelectedRow();
        String id = tbLoaiThucPham.getValueAt(row, 0).toString();
        String name = tbLoaiThucPham.getValueAt(row, 1).toString();

        tfIdThucPham.setText(id);
        tfLoaiThucPham.setText(name);
    }

    public void resetForm() {
        row = -1;
        tfIdThucPham.setText("");
        tfLoaiThucPham.setText("");
    }

